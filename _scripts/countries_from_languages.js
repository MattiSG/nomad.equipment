var alphaCodeToName = require('./ISO_3166-1_alpha-2.json')

module.exports = function(supportedLanguages) {
	var supportedCountries = [];

	for (i in supportedLanguages)
		supportedCountries = supportedCountries.concat(LANGUAGES_TO_COUNTRIES[supportedLanguages[i]]);

	supportedCountries = supportedCountries.sort().filter(function(val) { return val });  // remove nulls

	var supportedCodes = [];

	for (country of supportedCountries)
		for (code in alphaCodeToName)
			if (alphaCodeToName[code] == country)
				supportedCodes.push(code.toLowerCase());

	return Array.from(new Set(supportedCodes));
}
